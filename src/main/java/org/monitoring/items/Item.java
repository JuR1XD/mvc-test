package org.monitoring.items;

public class Item
{

	String heading;
	int result;

	public String getHeading()
	{
		return heading;
	}

	public int getResult()
	{
		return result;
	}

	public void setHeading(String heading)
	{
		this.heading = heading;
	}

	public void setResult(int result)
	{
		this.result = result;
	}
}
